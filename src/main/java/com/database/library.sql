CREATE DATABASE library_manager;
USE library_manager;

CREATE TABLE book (
	bookId INT PRIMARY KEY,
	bookName VARCHAR (20),
	author VARCHAR (20),
	description TEXT,
	quantity INT
);


CREATE TABLE student (
	stuId INT PRIMARY KEY,
	stuName VARCHAR (20),
	grade VARCHAR (10)
);

CREATE TABLE card (
	cardId INT PRIMARY KEY,
	bookId INT,
	stuId INT,
	FOREIGN KEY (bookId) REFERENCES book (bookId),
	FOREIGN KEY (stuId) REFERENCES student (stuId),
	status BOOL,
	loanDate DATE,
	returnDate DATE
);

